module.exports = {

    //Método para somar
    somar: (num1, num2) => num1 + num2,

    //Método para subtratir
    subtrair: (num1, num2) => num1 - num2,

    //Método para multiplicar
    multiplicar: (num1, num2) => num1 * num2,

    //Método para dividir
    dividir: (num1, num2) => num1 / num2,

};
